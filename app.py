import streamlit as st
import pandas as pd
import numpy as np
import joblib

# =====================================
# LOAD MODEL
# =====================================

model = joblib.load('model_gerd_rf.pkl')
scaler = joblib.load('scaler.pkl')
feature_names = joblib.load('feature_names.pkl')

# =====================================
# KONFIGURASI HALAMAN
# =====================================

st.set_page_config(
    page_title="Prediksi Risiko GERD",
    layout="centered"
)

# =====================================
# HEADER
# =====================================

st.title("Sistem Prediksi Risiko GERD")

st.write("""
Sistem ini digunakan untuk memprediksi risiko Gastroesophageal Reflux Disease (GERD)
berdasarkan pola makan menggunakan algoritma Random Forest.
""")

st.info("""
Keterangan Skala Frekuensi Konsumsi:

0 = Tidak Pernah  
1 = Jarang (1–2 kali per minggu)  
2 = Kadang-kadang (3–4 kali per minggu)  
3 = Sering (5–6 kali per minggu)  
4 = Setiap Hari  
""")

# =====================================
# INPUT USER
# =====================================

st.subheader("Input Data Pengguna")

st.markdown("### Jenis Diet")
st.caption("Isi dengan 1 = Ya dan 0 = Tidak")

diet_omn = st.number_input(
    "Diet Omnivore",
    min_value=0,
    max_value=1,
    value=0
)

diet_veg = st.number_input(
    "Diet Vegetarian",
    min_value=0,
    max_value=1,
    value=0
)

diet_vegt = st.number_input(
    "Diet Vegan",
    min_value=0,
    max_value=1,
    value=0
)

st.markdown("### Frekuensi Konsumsi")

fruit_frequency = st.number_input(
    "Frekuensi konsumsi buah",
    min_value=0,
    max_value=4,
    value=0,
    step=1
)

high_fat_red_meat_frequency = st.number_input(
    "Frekuensi konsumsi daging merah berlemak",
    min_value=0,
    max_value=4,
    value=0,
    step=1
)

homecooked_meals_frequency = st.number_input(
    "Frekuensi konsumsi makanan rumahan",
    min_value=0,
    max_value=4,
    value=0,
    step=1
)

vegetable_frequency = st.number_input(
    "Frekuensi konsumsi sayur",
    min_value=0,
    max_value=4,
    value=0,
    step=1
)

alcohol_frequency = st.number_input(
    "Frekuensi konsumsi alkohol",
    min_value=0,
    max_value=4,
    value=0,
    step=1
)

frozen_dessert_frequency = st.number_input(
    "Frekuensi konsumsi dessert beku",
    min_value=0,
    max_value=4,
    value=0,
    step=1
)

milk_cheese_frequency = st.number_input(
    "Frekuensi konsumsi susu dan keju",
    min_value=0,
    max_value=4,
    value=0,
    step=1
)

water_frequency = st.number_input(
    "Frekuensi konsumsi satu liter air per hari",
    min_value=0,
    max_value=4,
    value=0,
    step=1
)

salted_snacks_frequency = st.number_input(
    "Frekuensi konsumsi makanan asin",
    min_value=0,
    max_value=4,
    value=0,
    step=1
)

red_meat_frequency = st.number_input(
    "Frekuensi konsumsi daging merah",
    min_value=0,
    max_value=4,
    value=0,
    step=1
)

# =====================================
# PREDIKSI
# =====================================

if st.button("Prediksi Risiko GERD"):

    # =====================================
    # DATAFRAME INPUT
    # =====================================

    # Validasi diet
    diet_total = diet_omn + diet_veg + diet_vegt

    if diet_total != 1:
      st.error(
          "Pilih hanya satu jenis diet dengan nilai 1."
          )
      st.stop()

    input_data = pd.DataFrame([[
        diet_omn,
        diet_veg,
        diet_vegt,
        fruit_frequency,
        high_fat_red_meat_frequency,
        homecooked_meals_frequency,
        vegetable_frequency,
        alcohol_frequency,
        frozen_dessert_frequency,
        milk_cheese_frequency,
        water_frequency,
        salted_snacks_frequency,
        red_meat_frequency
    ]], columns=feature_names)

    # =====================================
    # NORMALISASI
    # =====================================

    input_scaled = scaler.transform(input_data)

    # =====================================
    # PREDIKSI
    # =====================================

    prediction = model.predict(input_scaled)[0]

    # =====================================
    # HASIL PREDIKSI
    # =====================================

    st.subheader("Hasil Prediksi")

    if prediction == 1:
        st.error("⚠️ Pengguna berisiko GERD")
    else:
        st.success("✅ Pengguna tidak berisiko GERD")
